
// week1_program1.cpp
// Program 1: Sum of Two Numbers
// Stores 50 and 100 in variables and stores their sum in a variable named total.

#include <iostream>
using namespace std;

int main() {
    int num1 = 50;     // first integer
    int num2 = 100;    // second integer
    int total = num1 + num2;  // sum of num1 and num2

    cout << "The total is: " << total << endl;

    return 0;
}
